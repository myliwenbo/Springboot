package vip.xjdai.mapper;

public class TUser {
    private Integer tbUserId;

    private String tbUserName;

    private String tbPassword;

    private String tbPhone;

    public Integer getTbUserId() {
        return tbUserId;
    }

    public void setTbUserId(Integer tbUserId) {
        this.tbUserId = tbUserId;
    }

    public String getTbUserName() {
        return tbUserName;
    }

    public void setTbUserName(String tbUserName) {
        this.tbUserName = tbUserName;
    }

    public String getTbPassword() {
        return tbPassword;
    }

    public void setTbPassword(String tbPassword) {
        this.tbPassword = tbPassword;
    }

    public String getTbPhone() {
        return tbPhone;
    }

    public void setTbPhone(String tbPhone) {
        this.tbPhone = tbPhone;
    }
}